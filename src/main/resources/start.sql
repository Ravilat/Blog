CREATE TABLE IF NOT EXISTS users
(
    id                INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    login             VARCHAR(50),
    password          VARCHAR(50),
    email             VARCHAR(50),
    date_registration TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);



