CREATE TABLE IF NOT EXISTS posts
(
    id          INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title       VARCHAR(50),
    content     TEXT,
    author_id   INTEGER,
    create_date TIMESTAMP,
    update_date TIMESTAMP,
    FOREIGN KEY (author_id) REFERENCES users (id)
);

